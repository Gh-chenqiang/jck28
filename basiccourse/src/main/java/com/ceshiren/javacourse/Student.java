package com.ceshiren.javacourse;

/**
 * @author chenqiang
 */
public class Student {

    private int snu;
    private String name;

    private String sex ;

    public int getSnu() {
        return snu;
    }

    public void setSnu(int snu) {
        this.snu = snu;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
